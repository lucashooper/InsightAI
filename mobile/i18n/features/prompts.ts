import { AppLanguage, TranslationTree } from '../types';

type PromptEntry = readonly [id: string, prompt: string, followUp?: string];

function toTranslationTree(entries: readonly PromptEntry[]): TranslationTree {
  return Object.fromEntries(
    entries.map(([id, prompt, followUp]) => {
      const copy: TranslationTree = { prompt };
      if (followUp) copy.followUp = followUp;
      return [id, copy];
    }),
  );
}

